# Дан признак в виде текстовых данных, и требуется удалить знаки препинания.

import unicodedata
import sys

text_data = {"Hi!!!! I. Love. This. Song......",
             "10000% Agree!!!! #LoveIT",
             "Right?!?!"}

# Создать словарь знаков препинания
puntuation = dict.fromkeys(i for i in range(sys.maxunicode)
                           if unicodedata.category(chr(i)).startswith("P"))

print([string.translate(puntuation) for string in text_data])

