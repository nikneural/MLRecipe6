# словарь punctuation, в котором в качестве ключей размещены все знаки препинания, присутствующие в Юникоде,
# и в качестве значений— None. Затем мы преобразовали все символы строкового значения, которые находятся среди знаков
# препинания, в None, фактически удалив их. Существуют более читаемые способы удаления знаков препинания,
# но это несколько хакерское решение имеет преимущество в том, что оно гораздо быстрее, чем другие альтернативы.
# Важно осознавать тот факт, что знаки препинания содержат информацию (например, сравните "правильно?" и
# "правильно!"). Удаление знаков препинания часто является необходимым злом для создания признаков; однако,
